%% m = makers()
%% 
%% Get a list of valid MATLAB plotting marker characters.
%%
%% Arguments:
%%  n    - integer number
%%
%% Returns:
%%  m    - marker character

% Copyright 2017 Grzegorz Cisek (grzegorzcisek@gmail.com)

function m = markers()
  m = ['+','o','*','.','x','s','d','^','v','>','<','p','h'];
end